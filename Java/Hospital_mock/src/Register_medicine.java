    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String datte = sdf.format(date.getDate());
            
         
            PreparedStatement add = con.prepareStatement("INSERT INTO medicine (id,name,exp) VALUES (?,?,?)");
            add.setString(1, id.getText());
            add.setString(2, name.getText());
            add.setString(3, datte);
            
            add.executeUpdate();
            JOptionPane.showMessageDialog(null,"The Medicine Has Been Added Successfully!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"The Medicine Connot be Added, Please Check The Fields Again!");
        }
       catch(Exception e){
       JOptionPane.showMessageDialog(null,"The Medicine Cannot be Added!");
       }
        
    }//GEN-LAST:event_jButton2ActionPerformed

